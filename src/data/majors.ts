export function detectMajorCategory(majorName: string): string | null {
  if (!majorName.trim()) return null;
  const name = majorName.toLowerCase();
  for (const [catId, cat] of Object.entries(majorCategoryMap)) {
    for (const kw of cat.keywords) {
      if (name.includes(kw.toLowerCase())) return catId;
    }
  }
  return null;
}

export function getTargetMajorIdsByUndergrad(majorName: string): string[] {
  const catId = detectMajorCategory(majorName);
  if (!catId) return majors.map((m) => m.id);
  return majorCategoryMap[catId].targetMajorIds;
}

export const getMajorById = (id: string): Major | undefined => majors.find((m) => m.id === id);
