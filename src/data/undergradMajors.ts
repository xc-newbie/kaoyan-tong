export function searchUndergradMajors(query: string): UndergradMajor[] {
  if (!query.trim()) return [];
  const q = query.trim().toLowerCase();
  return undergradMajors.filter(
    (m) => m.name.toLowerCase().includes(q) || m.category.includes(q)
  ).slice(0, 15);
}
